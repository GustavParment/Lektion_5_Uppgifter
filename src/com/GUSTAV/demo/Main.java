package com.GUSTAV.demo;

import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int x;

        System.out.println("Enter a number: ");
        x = sc.nextInt();

        for (int i = 0; i < x ; i++) {
            System.out.println("The loop will run " + x + " times");

        }





    }
}
